void gcomp( double  g[9], double  x[10] )
{
 int   i,j;

/* LP for Working Capital Management from Fylstra Ex. 4.  */
g[1] = 400000.0 - x[1] - x[7] - x[9] - 75000.0 ;
g[2] = g[1] + 1.01 * x[1] - x[2] + 10000.0 ;
g[3] = g[2] + 1.01 * x[2] - x[3] + 20000.0 ;
g[4] = g[3] + 1.01 * x[3] + 1.04 * x[7] - x[4] - x[8] - 80000.0 ;
g[5] = g[4] + 1.01 * x[4] - x[5] - 50000.0 ;
g[6] = g[5] + 1.01 * x[5] - x[6] + 15000.0 ;
g[7] = g[6] + 1.01 * x[6] + 1.04 * x[8] + 1.09 * x[9] - 60000.0 ;
g[8] = g[7] - 180000.0 ;

return;
}
